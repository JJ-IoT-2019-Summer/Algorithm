void DFS()
{
	int V = 1;
	C = 0;
	memset(visit, false, sizeof(visit));	// ?ьм ?ђї ????
	stack<int> s;
	s.push(V);

	while (!s.empty())
	{
		V = s.top();
		s.pop();
		if (!visit[V])	//???? ?ьм ???? ???
		{
			C++;
			visit[V] = true;
		}
		for (int i = 0;i < ad[V].size();i++)	//??? ??? ?ьм ???? ??? 
		{
			if (!visit[ad[V].at(i)])
				s.push(ad[V].at(i));
		}
	}
}


